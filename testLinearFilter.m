I = imread('lena.jpg');
%F = fspecial('gaussian', [5 5], 1.0);
%F = fspecial('laplacian',0);
F = fspecial('average', [5 5]);
J = linearFilter(I,F);
h1 = histogram(I);
h2 = histogram(J);
subplot(2,2,1); imshow(rgb2gray(I));  hold on;
subplot(2,2,2); imshow(J); hold on;
subplot(2,2,3); bar(h1); hold on;
subplot(2,2,4); bar(h2);